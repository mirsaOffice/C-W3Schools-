#include <iostream>
using namespace std;

int main() {
    //Pernyataan C++
    //Program komputer adalah daftar "intruksi" yang akan "dieksekusi" oleh komputer. Dalam bahasa pemrograman, intruksi pemrograman ini disebut statement (Pernyataan). Pernyataan berikut "memerintahkan" kompiler untuk mencetak teks "Hello World" ke layar :
    cout << "Hello World!";
    cout << "\n======================";

    //Banyak Pernyataan
    //Sebagian besar program C++ mengandung banyak pernyataan.
    //Pernyataan-pernyataan tersebut dieksekusi satu per satu, sesuai urutan penulisannya:
    cout << "\nHello World!";
    cout << "\nHave a good day!";
    cout << "\n======================";
}
