#include <iostream>
using namespace std;

int main(){
    //Contoh Kehidupan Nyata
    //Berikut adalah contoh nyata penggunaan tipe data berbeda untuk menghitung dan mengeluarkan total biaya sejumlah item:
    int items = 50;
    double cost_per_item = 9.99;
    double total_cost = items * cost_per_item;
    char currency = '$';
    cout << "Number of items: " << items << endl;
    cout << "Cost per item: " << cost_per_item << currency << endl;
    cout << "Total cost = " << total_cost << currency << endl;
    cout << "======================" << endl;
}