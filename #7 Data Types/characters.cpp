#include <iostream>
using namespace std;

int main(){
    //Tipe Karakter
    //Tipe data char digunakan untuk menyimpan satu karakter. karakter harus diapit oleh tanda kutip tunggalm seperti 'A' atau 'c':
    char myGrade = 'B';
    cout << myGrade << endl;
    cout << "======================" << endl;

    //Atau, jika Anda terbiasa dengan ASCII, Anda dapat menggunakan nilai ASCII untuk menampilkan karakter tertentu:
    char a = 65, b = 66, c = 67;
    cout << a << endl;
    cout << b << endl;
    cout << c << endl;
    cout << "======================" << endl;
}